public class EstruturaRepeticaoFor {
    public static void main(String[] args) {
        /*
         * 2 pontos e vírgula, Primeira seção declara e inicializa variável,
         * segunda seção verifica a condição pra saber se continua o loop
         * terceira, executada sempre no final da iteração e incrementa
         * */

        for(int count = 0 ; count <= 5 ; count++ ){
           System.out.println(count);

        }
    }
}
